export function renderAsciiArt(text: string, font: AsciiFont = "standard"): string[] {
  const upper = text.toUpperCase().slice(0, 20);
  const rows: string[] = [];
  const scale = font === "banner" ? 2 : 1;
  const block = font === "banner" ? "##" : font === "big" ? "\u2588\u2588" : "\u2588";
  const space = font === "banner" ? "  " : font === "big" ? "  " : " ";

  for (let row = 0; row < 7; row++) {
    let line = "";
    for (const ch of upper) {
      const pattern = FONT_5X7[ch] || FONT_5X7[" "];
      if (!pattern) {
        line += space.repeat(5) + space;
        continue;
      }
      const bits = pattern[row] || 0;
      for (let bit = 4; bit >= 0; bit--) {
        line += (bits >> bit) & 1 ? block : space;
      }
      line += space; // gap between chars
    }
    for (let s = 0; s < scale; s++) rows.push(line);
  }
  return rows;
}

